
package eva2_9_for;


public class EVA2_9_FOR {


    public static void main(String[] args) {
        //3 PARÁMETROS PARA FUNCIONAR
        //PUNTO INICIAL 
        //DONDE ACABA
        //COMO ME VOY A MOVER
        //i --> índice (arreglos)
        for(int i = 1; i <= 10; i++){
            System.out.println(i + "HOLA MUNDO!!!"); 
            System.out.println();
        }
        for(int i = 10; i >= 1; i--){
            System.out.println(i + "HOLA MUNDO!");
            System.out.println();
        }
    }
    
}
